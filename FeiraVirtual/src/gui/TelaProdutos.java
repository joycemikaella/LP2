    private void criaTabelaHistorico() throws ParseException {
        //cria o modelo da tabela
        DefaultTableModel modelo = (DefaultTableModel) tabelaHistorico.getModel();
        Object conteudoLinha[] = new Object[5];
        
        //adiciona os dados da compra na classe Pedido
        Pedido pedido = new Pedido(getCodigoProduto(),labelProd.getText(), getTotal(), (double) getValor_compra(), getQuantDesejada());

        //adiciona o pedido num ArrayList
        ArrayList<Pedido> listPedidos = new ArrayList<>();
        listPedidos.add(pedido);
        
        for(int i=0; i < listPedidos.size(); i++) {
            conteudoLinha[0] = listPedidos.get(i).getCodigo();
            conteudoLinha[1] = listPedidos.get(i).getNome();
            conteudoLinha[2] = listPedidos.get(i).getValor();
            conteudoLinha[3] = listPedidos.get(i).getValor_compra();
            conteudoLinha[4] = listPedidos.get(i).getQuantidade();
            modelo.addRow(conteudoLinha);
        }
        
        atualizarAposCompra();
    }
    
