    private String CalculaVendasTotal() {
        Double vendas = 0.0;
        for ( int i = 0; i < tabelaHistorico.getRowCount(); i++){
            vendas += Double.parseDouble(tabelaHistorico.getValueAt(i, 2).toString());
        }
        return vendas.toString();        
    }
    
    private String CalculaLucroReais() throws ParseException {
        Double investimento = Double.parseDouble(CalculaInvestimentoTotal());
        Double vendas = Double.parseDouble(CalculaVendasTotal());
        Double lucro = vendas - investimento;
        return lucro.toString();
    }
    
    private String CalculaLucroPorc() throws ParseException {
        Double lucro = Double.parseDouble(CalculaLucroReais());
        Double investimento = Double.parseDouble(CalculaInvestimentoTotal());
        Double porcentagem = lucro / investimento;
        return porcentagem.toString();
    }
    
