    private void removerProdutoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_removerProdutoMouseClicked
        //remove um produto da jTable
        if(tabelaProd.getSelectedRow() != -1) {
            DefaultTableModel modelo = (DefaultTableModel) tabelaProd.getModel();
            modelo.removeRow(tabelaProd.getSelectedRow());
            JOptionPane.showMessageDialog(null, "Produto removido com sucesso");
        } else {
            JOptionPane.showMessageDialog(null, "Nenhum produto selecionado. Selecione um produto da tabela para removê-lo");
        }
    }//GEN-LAST:event_removerProdutoMouseClicked

